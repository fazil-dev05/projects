--10.	Employee, department, and project together.

SELECT E.EMP_ID , E.EMP_NAME , E.DEPT_ID ,E.SALARY , D.DEPT_NAME ,P.PROJECT_NAME
FROM EMPLOYEE E , DEPARTMENT D , PROJECT P , EMPLOYEE_PROJECT EP
WHERE E.DEPT_ID = D.DEPT_ID
AND P.PROJECT_ID = EP.PROJECT_ID
AND E.EMP_ID = EP.	EMP_ID; --10
