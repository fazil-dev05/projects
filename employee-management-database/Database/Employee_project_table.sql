    EMP_ID NUMBER,
    PROJECT_ID NUMBER,
    HOURS_WORKED NUMBER,

    CONSTRAINT PK_EMP_PROJECT
    PRIMARY KEY (EMP_ID, PROJECT_ID),

    CONSTRAINT FK_EP_EMP
    FOREIGN KEY (EMP_ID)
    REFERENCES EMPLOYEE(EMP_ID),

    CONSTRAINT FK_EP_PROJECT
    FOREIGN KEY (PROJECT_ID)
    REFERENCES PROJECT(PROJECT_ID)
);

